class GuestMirror:
    def reflect(self, diagnosis):
        # Ироничный и точный фидбек на основе выявленных разрывов
        gap = diagnosis.get('vertical_gap', 0)
        if gap > 1.5:
            return "Твой интеллект (Пиаже) — гоночный болид, но моральные тормоза (Кольберг) остались в гараже. Осторожнее на поворотах."
        return "Твоя личность сбалансирована. Ты видишь систему и умеешь в ней действовать."